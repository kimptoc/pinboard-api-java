package net.kimptoc.pinboard;

public class Tag {
    private String tagName;
    private String tagCount;

    public String getTagName() {
        return tagName;
    }

    public void setTagName(String textContent) {
        tagName = textContent;
    }

    public String getTagCount() {
        return tagCount;
    }

    public void setCount(String tag) {
        tagCount = tag;
    }

}
